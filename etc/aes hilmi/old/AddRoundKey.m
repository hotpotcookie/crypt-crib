function s_out = AddRoundKey(s_in,round_key)
%s_in= [0x04 0xe0 0x48 0x28; 0x66 0xcb 0xf8 0x06; 0x81 0x19 0xd3 0x26; 0xe5 0x9a 0x7a 0x4c];
%round_key= [0xa0 0x88 0x23 0x2a; 0xfa 0x54 0xa3 0x6c; 0xfe 0x2c 0x39 0x76; 0x17 0xb1 0x39 0x05];
s_out = bitxor(s_in, round_key);
%s_out=dec2hex(s_out);
end